/**
 * Centralized API Test Service
 * Covers: Auth, Project, Task, Agent, Engine, Report, Health
 *
 * Each test returns: { module, action, success, data, error, duration }
 * runAllTests(opts) runs sequentially and supports stopOnFailure option.
 */

import apiClient from '../index.js';

// ─── Helpers ──────────────────────────────────────────────────────────────────

const makeResult = (module, action, success, data = null, error = null, duration = 0) => ({
  module,
  action,
  success,
  data,
  error,
  duration,
});

const timed = async (fn) => {
  const start = performance.now();
  const result = await fn();
  const duration = Math.round(performance.now() - start);
  return { ...result, duration };
};

// Store shared IDs across tests
let sharedToken = null;
let sharedProjectId = null;
let sharedTaskId = null;

// ─── Test Credentials ─────────────────────────────────────────────────────────
// Configurable — override before calling runAllTests() if needed
export const testCredentials = {
  name: 'Test User',
  email: 'testuser_auto@example.com',
  password: 'Test@12345',
};

// ─── Health Check ─────────────────────────────────────────────────────────────

export const testHealth = async () =>
  timed(async () => {
    try {
      const { data } = await apiClient.get('/health');
      return makeResult('system', 'health', true, data);
    } catch (err) {
      return makeResult('system', 'health', false, null, err.message);
    }
  });

// ─── Auth Module ──────────────────────────────────────────────────────────────

export const testRegister = async () =>
  timed(async () => {
    try {
      const { data } = await apiClient.post('/auth/register', testCredentials);
      return makeResult('auth', 'register', true, data);
    } catch (err) {
      // 409 Conflict = already registered, treat as acceptable
      const isKnown = err.message?.includes('already') || err.message?.includes('exists');
      return makeResult('auth', 'register', isKnown, null, err.message);
    }
  });

export const testLogin = async () =>
  timed(async () => {
    try {
      const { data } = await apiClient.post('/auth/login', {
        email: testCredentials.email,
        password: testCredentials.password,
      });
      const token = data.token || data.accessToken;
      if (token) {
        sharedToken = token;
        localStorage.setItem('token', token);
      }
      const valid = !!token && typeof token === 'string' && token.split('.').length === 3;
      return makeResult('auth', 'login', valid, { token: token?.slice(0, 20) + '…', valid });
    } catch (err) {
      return makeResult('auth', 'login', false, null, err.message);
    }
  });

export const testForgotPassword = async () =>
  timed(async () => {
    try {
      const { data } = await apiClient.post('/auth/forgot-password', { email: testCredentials.email });
      return makeResult('auth', 'forgotPassword', true, data);
    } catch (err) {
      return makeResult('auth', 'forgotPassword', false, null, err.message);
    }
  });

