// ─── Run All Tests ────────────────────────────────────────────────────────────

/**
 * Run all tests sequentially.
 * @param {object} opts
 * @param {boolean} opts.stopOnFailure - Stop the entire suite on first failure (default: false)
 * @param {function} opts.onResult - Callback invoked after each result for live streaming
 */
export const runAllTests = async ({ stopOnFailure = false, onResult = null } = {}) => {
  const results = [];

  const run = async (label, testFn) => {
    const result = await testFn();
    const normalized = Array.isArray(result) ? result : [result];
    results.push(...normalized);
    if (onResult) normalized.forEach(r => onResult(r));
    return normalized;
  };

  const shouldStop = (batch) => stopOnFailure && batch.some(r => !r.success);

  // 1. Health Check
  const healthBatch = await run('health', testHealth);
  if (shouldStop(healthBatch)) return results;

  // 2. Auth: register → login
  await run('register', testRegister);
  const loginBatch = await run('login', testLogin);
  if (shouldStop(loginBatch)) return results;

  // 3. Projects (full CRUD)
  const createProjectBatch = await run('createProject', testCreateProject);
  if (shouldStop(createProjectBatch)) return results;
  await run('getProjects', testGetProjects);
  await run('getSingleProject', testGetSingleProject);
  await run('updateProject', testUpdateProject);

  // 4. Tasks (CRUD before deleting project)
  const createTaskBatch = await run('createTask', testCreateTask);
  if (shouldStop(createTaskBatch)) return results;
  await run('getTasksByProject', testGetTasksByProject);
  await run('updateTask', testUpdateTask);
  await run('deleteTask', testDeleteTask);

  // 5. Agents & Engines
  await run('agents', testAgents);
  await run('engines', testEngines);

  // 6. Report
  await run('createReport', testCreateReport);

  // 7. Clean up — delete project last
  await run('deleteProject', testDeleteProject);

  return results;
};
